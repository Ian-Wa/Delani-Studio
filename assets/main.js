$(document).ready(function(){
    $("#design_image").click(function(){
      $("#design_image").slideDown('1500').hide('1000');
      $("#design_details").show('1500');
    });
    $("#design_details").click(function(){
      $("#design_details").slideUp('1500');
      $("#design_image").slideDown('1500');
    });
  });

